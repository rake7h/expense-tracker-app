package com.bharatonjava.expense.domain;

public class Stat {

	private String item;
	private double total;
	
	public Stat() {
	}
	
	public String getItem() {
		return item;
	}
	
	public void setItem(String item) {
		this.item = item;
	}
	
	public double getTotal() {
		return total;
	}
	
	public void setTotal(double total) {
		this.total = total;
	}
	
	
}
